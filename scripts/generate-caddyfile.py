#!/usr/bin/env python3
"""Generate Caddyfile from app manifests.

Usage:
  python3 scripts/generate-caddyfile.py [output_path]

Reads all manifest.json files in the repo root and generates a Caddyfile.
Special cases (admin with basic auth, anchor with path routing) are hardcoded.
All other apps get a standard subdomain reverse_proxy block.

Set APP_HOST env var to change the domain (default: 6cubed.app).
"""
import sys
import json
import os
from urllib.parse import quote

script_dir = os.path.dirname(os.path.abspath(__file__))
repo_root = os.path.dirname(script_dir)

domain = os.environ.get("APP_HOST", "6cubed.app")
output = sys.argv[1] if len(sys.argv) > 1 else os.path.join(repo_root, "Caddyfile")

lines = [
    "# 216labs — Caddy reverse proxy (subdomain-based routing)",
    "# AUTO-GENERATED by scripts/generate-caddyfile.py — do not edit manually",
    "#",
    f"# Each app is served at {{id}}.{domain} — Caddy auto-provisions",
    "# HTTPS certificates via Let's Encrypt.",
    "",
    f"admin.{domain} {{",
    "\tbasicauth {",
    "\t\t{env.ADMIN_USER} {env.ADMIN_PASSWORD_HASH}",
    "\t}",
    "\treverse_proxy admin:3000",
    "}",
    "",
    f"anchor.{domain} {{",
    "\thandle /api/* {",
    "\t\treverse_proxy anchor-api:8000",
    "\t}",
    "\thandle {",
    "\t\treverse_proxy anchor-web:80",
    "\t}",
    "}",
    "",
]

skip_ids = {"admin", "anchor"}
entries = []
root_domain_app = None  # (docker_svc, port) for 6cubed.app + www.6cubed.app

def manifest_dirs():
    # Top-level (admin, happypath, pipesecure, etc.)
    for entry in os.listdir(repo_root):
        path = os.path.join(repo_root, entry)
        if os.path.isdir(path) and not entry.startswith("."):
            yield path
    # Apps under apps/
    apps_dir = os.path.join(repo_root, "apps")
    if os.path.isdir(apps_dir):
        for entry in os.listdir(apps_dir):
            path = os.path.join(apps_dir, entry)
            if os.path.isdir(path) and not entry.startswith("."):
                yield path

for dir_path in manifest_dirs():
    manifest_path = os.path.join(dir_path, "manifest.json")
    if not os.path.isfile(manifest_path):
        continue
    try:
        with open(manifest_path) as f:
            m = json.load(f)
        app_id = m.get("id")
        if not app_id or app_id in skip_ids:
            continue
        docker_svc = m.get("docker_service", app_id)
        internal_port = m.get("internal_port", 3000)
        if m.get("root_domain"):
            root_domain_app = (docker_svc, internal_port)
        entries.append((app_id, docker_svc, internal_port))
    except Exception as e:
        print(f"Warning: could not read {manifest_path}: {e}", file=sys.stderr)

entries.sort(key=lambda x: x[0])

# Root domain (6cubed.app + www.6cubed.app) → landing app
if root_domain_app:
    svc, port = root_domain_app
    lines += [
        f"{domain} {{",
        f"\treverse_proxy {svc}:{port}",
        "}",
        "",
        f"www.{domain} {{",
        f"\treverse_proxy {svc}:{port}",
        "}",
        "",
    ]

# On cold upstream (502/503/504), send users to Activator warmup so docker compose can start the app.
# Skip activator itself and manifests with internal_port 0 (no HTTP upstream).
for app_id, docker_svc, port in entries:
    try:
        p = int(port)
    except (TypeError, ValueError):
        p = 0
    use_warmup = app_id != "activator" and p > 0
    if use_warmup:
        dest_enc = quote(f"https://{app_id}.{domain}", safe="")
        warm = f"https://activator.{domain}/warmup?app={app_id}&dest={dest_enc}"
        lines += [
            f"{app_id}.{domain} {{",
            "\thandle_errors {",
            "\t\t@cold expression `{err.status_code} == 502 || {err.status_code} == 503 || {err.status_code} == 504`",
            "\t\thandle @cold {",
            f'\t\t\tredir "{warm}" 302',
            "\t\t}",
            "\t}",
            f"\treverse_proxy {docker_svc}:{port}",
            "}",
            "",
        ]
    else:
        lines += [
            f"{app_id}.{domain} {{",
            f"\treverse_proxy {docker_svc}:{port}",
            "}",
            "",
        ]

with open(output, "w") as f:
    f.write("\n".join(lines) + "\n")

print(f"Generated {output} ({len(entries)} app entries + admin + anchor)")
